def google_user_uuid(google_subject: str, display_email: str) -> str:
    seed = google_subject or display_email.lower().strip()
    return str(uuid.uuid5(uuid.NAMESPACE_URL, f'google:{seed}'))


def build_scan_history_record(
    user_uuid: str,
    scan_result: Dict[str, Any],
    source: str = 'gmail',
    timestamp: Optional[str] = None,
) -> Dict[str, Any]:
    result = scan_result if isinstance(scan_result, dict) else {}
    flags = result.get('flags', []) if isinstance(result.get('flags', []), list) else []
    return {
        'user_uuid': user_uuid,
        'source': source,
        'sender': result.get('sender') or 'Unknown Sender',
        'subject': result.get('subject') or 'No Subject',
        'risk_score': int(result.get('risk_score') or 0),
        'timestamp': timestamp or result.get('received_at') or time.strftime('%Y-%m-%dT%H:%M:%SZ', time.gmtime()),
        'triggered_flags': flags,
        'message_id': result.get('message_id') or '',
        'thread_id': result.get('thread_id'),
        'status': result.get('status', 'clean'),
    }


def _request_json(base_url: str, service_role_key: str, method: str, path: str, params: Optional[Dict[str, Any]] = None, body: Optional[Any] = None) -> Any:
    normalized_base_url = base_url.rstrip('/')
    request_url = normalized_base_url + path
    if params:
        request_url += '?' + urllib.parse.urlencode(params, doseq=True)

    request_body = None
    headers = {
        'apikey': service_role_key,
        'Authorization': f'Bearer {service_role_key}',
        'Accept': 'application/json',
        'Content-Type': 'application/json',
    }
    if body is not None:
        request_body = json.dumps(body).encode('utf-8')

    request = urllib.request.Request(request_url, data=request_body, headers=headers, method=method)
    try:
        with urllib.request.urlopen(request, timeout=20) as response:
            response_text = response.read().decode('utf-8')
            if not response_text.strip():
                return None
            return json.loads(response_text)
    except urllib.error.HTTPError as exc:
        error_body = exc.read().decode('utf-8', errors='replace') if exc.fp else str(exc)
        raise RuntimeError(f'Supabase request failed: {error_body}') from exc
    except urllib.error.URLError as exc:
        raise RuntimeError(f'Supabase request failed: {exc}') from exc


class SupabaseStore:
    def __init__(self, base_url: str, service_role_key: str) -> None:
        self.base_url = base_url.rstrip('/')
        self.service_role_key = service_role_key

    @classmethod
    def from_env(cls) -> 'SupabaseStore':
        base_url = os.getenv('SUPABASE_URL', '').strip()
        service_role_key = os.getenv('SUPABASE_SERVICE_ROLE_KEY', '').strip()
        if not base_url or not service_role_key:
            raise RuntimeError('SUPABASE_URL and SUPABASE_SERVICE_ROLE_KEY must be set')
        return cls(base_url, service_role_key)

    def _request(self, method: str, path: str, params: Optional[Dict[str, Any]] = None, body: Optional[Any] = None) -> Any:
        return _request_json(self.base_url, self.service_role_key, method, path, params=params, body=body)

    def get_user(self, user_uuid: str) -> Optional[Dict[str, Any]]:
        results = self._request(
            'GET',
            '/rest/v1/users',
            params={'select': '*', 'user_uuid': f'eq.{user_uuid}', 'limit': 1},
        )
        if isinstance(results, list) and results:
            first_result = results[0]
            return first_result if isinstance(first_result, dict) else None
        return None

    def list_active_users(self) -> List[Dict[str, Any]]:
        results = self._request(
            'GET',
            '/rest/v1/users',
            params={
                'select': 'user_uuid,display_email,encrypted_refresh_token,active_monitoring',
                'active_monitoring': 'eq.true',
                'order': 'updated_at.asc',
            },
        )
        return [item for item in results if isinstance(item, dict)] if isinstance(results, list) else []

    def upsert_user(self, user_row: Dict[str, Any]) -> Dict[str, Any]:
        results = self._request(
            'POST',
            '/rest/v1/users',
            params={'on_conflict': 'user_uuid'},
            body=[user_row],
        )
        if isinstance(results, list) and results:
            first_result = results[0]
            if isinstance(first_result, dict):
                return first_result
        return user_row

    def list_user_scans(self, user_uuid: str, limit: int = 25) -> List[Dict[str, Any]]:
        results = self._request(
            'GET',
            '/rest/v1/scan_history',
            params={
                'select': '*',
                'user_uuid': f'eq.{user_uuid}',
                'order': 'timestamp.desc',
                'limit': str(limit),
            },
        )
        return [item for item in results if isinstance(item, dict)] if isinstance(results, list) else []

    def upsert_scan_history(self, scan_row: Dict[str, Any]) -> Dict[str, Any]:
        results = self._request(
            'POST',
            '/rest/v1/scan_history',
            params={'on_conflict': 'user_uuid,message_id'},
            body=[scan_row],
        )
        if isinstance(results, list) and results:
            first_result = results[0]
            if isinstance(first_result, dict):
                return first_result
        return scan_row
