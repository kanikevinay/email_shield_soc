def _read_json_file(path: Path) -> Dict[str, Any]:
    if not path.exists():
        return {}
    try:
        with path.open('r', encoding='utf-8') as file_handle:
            data = json.load(file_handle)
    except (OSError, ValueError, json.JSONDecodeError):
        return {}
    return data if isinstance(data, dict) else {}


def _app_secret(env_name: str, fallback: str) -> str:
    value = os.getenv(env_name)
    if value:
        return value
    return fallback


def _derive_key(secret: str) -> bytes:
    return hashlib.sha256(secret.encode('utf-8')).digest()


def _xor_bytes(payload: bytes, key_stream: bytes) -> bytes:
    return bytes(byte ^ key_stream[index] for index, byte in enumerate(payload))


def _keystream(key: bytes, nonce: bytes, length: int) -> bytes:
    chunks: List[bytes] = []
    counter = 0
    while sum(len(chunk) for chunk in chunks) < length:
        chunks.append(hmac.new(key, nonce + counter.to_bytes(4, 'big'), hashlib.sha256).digest())
        counter += 1
    return b''.join(chunks)[:length]


def encrypt_refresh_token(refresh_token: str, secret: Optional[str] = None) -> str:
    token_secret = _derive_key(secret or _app_secret('APP_TOKEN_ENCRYPTION_KEY', 'email-shield-dev-token-secret'))
    nonce = secrets.token_bytes(16)
    payload = refresh_token.encode('utf-8')
    cipher_text = _xor_bytes(payload, _keystream(token_secret, nonce, len(payload)))
    digest = hmac.new(token_secret, nonce + cipher_text, hashlib.sha256).digest()
    return base64.urlsafe_b64encode(nonce + cipher_text + digest).decode('ascii')


def decrypt_refresh_token(cipher_text: str, secret: Optional[str] = None) -> str:
    token_secret = _derive_key(secret or _app_secret('APP_TOKEN_ENCRYPTION_KEY', 'email-shield-dev-token-secret'))
    try:
        raw_payload = base64.urlsafe_b64decode(cipher_text.encode('ascii'))
    except (ValueError, UnicodeDecodeError) as exc:
        raise ValueError('Invalid encrypted refresh token payload') from exc

    if len(raw_payload) < 16 + 32:
        raise ValueError('Encrypted refresh token payload is too short')

    nonce = raw_payload[:16]
    stored_cipher_text = raw_payload[16:-32]
    stored_digest = raw_payload[-32:]
    expected_digest = hmac.new(token_secret, nonce + stored_cipher_text, hashlib.sha256).digest()
    if not hmac.compare_digest(stored_digest, expected_digest):
        raise ValueError('Encrypted refresh token integrity check failed')

    plain_text = _xor_bytes(stored_cipher_text, _keystream(token_secret, nonce, len(stored_cipher_text)))
    return plain_text.decode('utf-8')


def sign_payload(payload: Dict[str, Any], secret: Optional[str] = None) -> str:
    session_secret = _derive_key(secret or _app_secret('APP_SESSION_SECRET', 'email-shield-dev-session-secret'))
    payload_json = json.dumps(payload, separators=(',', ':'), sort_keys=True).encode('utf-8')
    signature = hmac.new(session_secret, payload_json, hashlib.sha256).digest()
    encoded_payload = base64.urlsafe_b64encode(payload_json).decode('ascii').rstrip('=')
    encoded_signature = base64.urlsafe_b64encode(signature).decode('ascii').rstrip('=')
    return f'{encoded_payload}.{encoded_signature}'


def create_csrf_state() -> str:
    return sign_payload({'nonce': secrets.token_urlsafe(24), 'issued_at': int(time.time())}, secret='oauth-state')


def validate_csrf_state(token: str, max_age_seconds: int = 600) -> Dict[str, Any]:
    payload = verify_signed_payload(token, secret='oauth-state')
    issued_at = int(payload.get('issued_at', 0) or 0)
    if max_age_seconds and int(time.time()) - issued_at > max_age_seconds:
        raise ValueError('OAuth state has expired')
    return payload


def verify_signed_payload(token: str, secret: Optional[str] = None) -> Dict[str, Any]:
    session_secret = _derive_key(secret or _app_secret('APP_SESSION_SECRET', 'email-shield-dev-session-secret'))
    try:
        encoded_payload, encoded_signature = token.split('.', 1)
        payload_bytes = base64.urlsafe_b64decode(encoded_payload + '=' * (-len(encoded_payload) % 4))
        signature_bytes = base64.urlsafe_b64decode(encoded_signature + '=' * (-len(encoded_signature) % 4))
    except (ValueError, UnicodeDecodeError, base64.binascii.Error) as exc:
        raise ValueError('Invalid signed payload format') from exc

    expected_signature = hmac.new(session_secret, payload_bytes, hashlib.sha256).digest()
    if not hmac.compare_digest(signature_bytes, expected_signature):
        raise ValueError('Signed payload verification failed')

    data = json.loads(payload_bytes.decode('utf-8'))
    if not isinstance(data, dict):
        raise ValueError('Signed payload must decode to a JSON object')
    return data


def generate_state_token() -> str:
    return secrets.token_urlsafe(32)


def _credentials_config() -> Dict[str, Any]:
    config = _read_json_file(CREDENTIALS_FILE)
    web_config = config.get('web') if isinstance(config.get('web'), dict) else {}
    installed_config = config.get('installed') if isinstance(config.get('installed'), dict) else {}
    merged = {**installed_config, **web_config}
    return {
        'client_id': os.getenv('GOOGLE_CLIENT_ID') or merged.get('client_id', ''),
        'client_secret': os.getenv('GOOGLE_CLIENT_SECRET') or merged.get('client_secret', ''),
        'auth_uri': merged.get('auth_uri', 'https://accounts.google.com/o/oauth2/v2/auth'),
        'token_uri': os.getenv('GOOGLE_TOKEN_URI') or merged.get('token_uri', GOOGLE_TOKEN_URL),
        'redirect_uris': merged.get('redirect_uris', []),
        'project_id': merged.get('project_id', ''),
    }


def get_google_client_config() -> Dict[str, Any]:
    config = _credentials_config()
    if not config['client_id'] or not config['client_secret']:
        raise RuntimeError('Google OAuth client configuration is missing in config/credentials.json or environment variables.')
    return config


def resolve_google_redirect_uri(request_base_url: Optional[str] = None) -> str:
    configured = os.getenv('GOOGLE_OAUTH_REDIRECT_URI', '').strip()
    if configured:
        return configured
    config = _credentials_config()
    redirect_uris = config.get('redirect_uris') or []
    if isinstance(redirect_uris, list) and redirect_uris:
        first_uri = redirect_uris[0]
        if isinstance(first_uri, str) and first_uri.strip():
            return first_uri.strip()
    if request_base_url:
        return request_base_url.rstrip('/') + '/auth/callback'
    return 'http://127.0.0.1:8000/auth/callback'


def build_google_authorization_url(redirect_uri: str, state: str, scopes: Optional[List[str]] = None) -> str:
    config = get_google_client_config()
    authorization_params = {
        'response_type': 'code',
        'client_id': config['client_id'],
        'redirect_uri': redirect_uri,
        'scope': ' '.join(scopes or GOOGLE_GMAIL_SCOPES),
        'access_type': 'offline',
        'prompt': 'consent',
        'include_granted_scopes': 'true',
        'state': state,
    }
    return config['auth_uri'] + '?' + urllib.parse.urlencode(authorization_params)


def exchange_google_authorization_code(code: str, redirect_uri: str) -> Dict[str, Any]:
    config = get_google_client_config()
    payload = urllib.parse.urlencode(
        {
            'code': code,
            'client_id': config['client_id'],
            'client_secret': config['client_secret'],
            'redirect_uri': redirect_uri,
            'grant_type': 'authorization_code',
        }
    ).encode('utf-8')
    request = urllib.request.Request(
        config.get('token_uri') or GOOGLE_TOKEN_URL,
        data=payload,
        headers={'Content-Type': 'application/x-www-form-urlencoded', 'Accept': 'application/json'},
        method='POST',
    )
    try:
        with urllib.request.urlopen(request, timeout=20) as response:
            response_body = response.read().decode('utf-8')
    except urllib.error.HTTPError as exc:
        error_body = exc.read().decode('utf-8', errors='replace') if exc.fp else str(exc)
        raise RuntimeError(f'Google token exchange failed: {error_body}') from exc
    except urllib.error.URLError as exc:
        raise RuntimeError(f'Google token exchange failed: {exc}') from exc

    data = json.loads(response_body)
    if not isinstance(data, dict):
        raise RuntimeError('Google token exchange returned an unexpected payload')
    return data


